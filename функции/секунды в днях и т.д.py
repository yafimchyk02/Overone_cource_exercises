def func(x):
    if x < 60:
        return '0 часов', 'o минут', x,'секунд'
    if x >= 60 and x < 3600:
        return 'о часов', x // 60,'минут', (x % 60), 'cекунд'
    if x >= 3600:
        return x // 3600, 'часов', (x % 3600) // 60, 'минут',(x % 3600) % 60,'секунд'

print(func(int(input('Введите количесво секунд: '))))